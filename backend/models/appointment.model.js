import mongoose from "mongoose";
const appointmentSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  phone: {
    type: Number,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  message: {
    type: String,
    required: true,
  },
});

const appointment = mongoose.model("appointment", appointmentSchema);
export default appointment;
